    public void showDataTransaksi()
    {
       
        List<Transaksi> Data=TC.showDataTransaksi();
        
        for(int i=0;i<Data.size();i++)
        {
           Object [] data = new Object[6];
                data[0] = Data.get(i).getID_Transaksi();
                data[1] = Data.get(i).getPembeli();
                data[2] = Data.get(i).getJudul();
                data[3] = Data.get(i).getHarga(); 
                data[4] = Data.get(i).getJumlah();
                data[5] = Data.get(i).getTotal();
            
                tb.addRow(data);
        }
    }
